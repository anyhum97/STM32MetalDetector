#define BufferSize 4096

#define PartSize 1024

#define BinaryLog 12

#define Half BufferSize >> 1

float AdcBuffer[BufferSize];

float ReBuffer[BufferSize];
float ImBuffer[BufferSize];

unsigned int Index = 0;

unsigned int IsReady = 1;

float Sqrt(float x)
{
	if(x <= 0.0f)
	{
		return 0.0f;
	}
  
	float a = 3.162278f;
  
	if(x >= 500.0f)
	{
		a = 31.62278f;
	}

	if(x >= 50000.0f)
	{
		a = 316.2278f;
	}
  
	for(char i=0; i<7; ++i)
	{
		a = 0.5f*(a+x/a);

		if(a*a == x)
		{
      break;
		}
	}
  
	return a;
}

void SetBuffers()
{
  for(int i=0; i<BufferSize; ++i)
  {
    AdcBuffer[i] = 0.0f;    
    ReBuffer[i] = 0.0f;
    ImBuffer[i] = 0.0f;
  }
}

void ShiftAdcBuffer()
{
  for(int i=0; i<BufferSize; ++i)
  {
    ReBuffer[i] = AdcBuffer[i];
    ImBuffer[i] = 0.0f;    
  }
  
  for(int i=0; i<BufferSize-PartSize; ++i)
  {
  	AdcBuffer[i] = AdcBuffer[i+PartSize];
  }
  
  for(int i=BufferSize-PartSize; i<BufferSize; ++i)
  {
  	AdcBuffer[i] = 0;
  }
  
  Index = BufferSize-PartSize;
}

void Compute()
{
  if(FFT(ReBuffer, ImBuffer, BufferSize, BinaryLog, FT_DIRECT) == 1)
  {
    HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14, GPIO_PIN_RESET);
    
    float max = 0.0f;

    float middle = 0.0f;
    
    int index = 0;
    
    for(int i=0; i<8; ++i)
    {
      ImBuffer[i] = 0.0;
    }
    
    for(int i=Half; i<BufferSize; ++i)
    {
      ImBuffer[i] = 0.0;
    }
    
    for(int i=8; i<Half; ++i)
    {
      float value = Sqrt(ReBuffer[i]*ReBuffer[i] + ImBuffer[i]*ImBuffer[i]);
      
      middle += value;
      
      if(value > max)
      {
        max = value;
        
        index = i;
      }

      ImBuffer[i] = value;
    }
    
    ImBuffer[0] = max;
    
    ImBuffer[1] = index;
    
    ImBuffer[2] = middle;
    
    ImBuffer[3] = ImBuffer[256];
    
    middle /= Half;
    
    HAL_GPIO_WritePin(GPIOG, GPIO_PIN_13, GPIO_PIN_RESET);
    
    if(ImBuffer[256] > 10.0f*middle)
    {
       HAL_GPIO_WritePin(GPIOG, GPIO_PIN_13, GPIO_PIN_SET);
    }

    //if(index >= 635 && index <= 645)
    //{
    //  HAL_GPIO_WritePin(GPIOG, GPIO_PIN_13, GPIO_PIN_SET);
    //}
  }
  else
  {
    HAL_GPIO_WritePin(GPIOG, GPIO_PIN_14, GPIO_PIN_SET);
  }
}

void UpdateBufferValue()
{
  if(Index == BufferSize)
  {
    if(IsReady)
    {
      IsReady = 0;
      ShiftAdcBuffer();
      Compute();
      IsReady = 1;
    }
  }
  else
  {
    HAL_ADC_Start(&hadc1);
    AdcBuffer[Index] = (unsigned short)HAL_ADC_GetValue(&hadc1);
    HAL_ADC_Stop(&hadc1);
    ++Index;
  }
}

